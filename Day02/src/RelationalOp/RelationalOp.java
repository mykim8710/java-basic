package RelationalOp;

public class RelationalOp {
	public static void main(String[] args) {
		System.out.println("3 >= 2 : " +(3 >= 2));
		System.out.println("3 <= 2 : " +(3 <= 2));
		System.out.println("7.0 == 7 : " +(7.0 == 7)); // 7을 double형으로 형변환하여 연산을 진행
		System.out.println("7.0 != 7 : " +(7.0 != 7));
	}
}
