package ArithOp;

public class ArithOp {
	public static void main(String[] args) {
		double a = 7.0;
		double b = 3.0;
		
		System.out.println("a + b = " +(a + b));
		System.out.println("a - b = " +(a - b));
		System.out.println("a * b = " +(a * b));
		System.out.println("a / b = " +(a / b));
		System.out.println("a % b = " +(a % b));
	}
}


